// ---------------------------------------------------------------------------------------------------------------------
// Notes Store
// ---------------------------------------------------------------------------------------------------------------------

import { defineStore } from 'pinia';

// Models
import { NotebookPage } from '../models/notebook';

// ---------------------------------------------------------------------------------------------------------------------

export interface NotebookStoreState
{
    id : string | null;
    pages : NotebookPage[];
}

// ---------------------------------------------------------------------------------------------------------------------

export const useNotebookStore = defineStore('notes', {
    state() : NotebookStoreState
    {
        return {
            id: null,
            pages: []
        };
    }
});

