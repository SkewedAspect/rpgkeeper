import axios from 'axios';

// Interfaces
import { SystemDefinition } from '@rpgk/core';

//----------------------------------------------------------------------------------------------------------------------

class SystemsResourceAccess
{
    async loadSystems() : Promise<SystemDefinition<Record<string, unknown>>[]>
    {
        const { data } = await axios.get('/api/systems');
        return data;
    }
}

//----------------------------------------------------------------------------------------------------------------------

export default new SystemsResourceAccess();

