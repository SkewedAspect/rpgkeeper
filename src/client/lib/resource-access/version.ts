import axios from 'axios';

// Models
import { AppVersion } from '@rpgk/core/models/version';

//----------------------------------------------------------------------------------------------------------------------

class VersionResourceAccess
{
    async getAppVersion() : Promise<AppVersion>
    {
        const { data } = await axios.get('/version');
        return data;
    }
}

//----------------------------------------------------------------------------------------------------------------------

export default new VersionResourceAccess();

