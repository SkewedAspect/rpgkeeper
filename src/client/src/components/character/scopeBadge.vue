<!----------------------------------------------------------------------------------------------------------------------
  -- scopeBadge
  --------------------------------------------------------------------------------------------------------------------->

<template>
    <BBadge class="supp-scope-badge" :variant="badgeVariant">
        {{ badgeText }}
    </BBadge>
</template>

<!--------------------------------------------------------------------------------------------------------------------->

<script lang="ts" setup>
    import { computed } from 'vue';

    // Models
    import type { Supplement } from '@rpgk/core';

    //------------------------------------------------------------------------------------------------------------------
    // Component Definition
    //------------------------------------------------------------------------------------------------------------------

    interface Props
    {
        supplement : Pick<Supplement, 'official'>
    }

    const props = defineProps<Props>();

    //------------------------------------------------------------------------------------------------------------------
    // Computed
    //------------------------------------------------------------------------------------------------------------------

    const badgeText = computed(() =>
    {
        return props.supplement.official ? 'Official' : 'Homebrew';
    });

    const badgeVariant = computed(() =>
    {
        return props.supplement.official ? 'info' : 'success';
    });

</script>

<!--------------------------------------------------------------------------------------------------------------------->
