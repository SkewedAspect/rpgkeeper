import axios from 'axios';

// Interfaces
import type { Role } from '@rpgk/core';

//----------------------------------------------------------------------------------------------------------------------

class RolesResourceAccess
{
    async list() : Promise<Role[]>
    {
        const { data } = await axios.get('/api/roles');
        return data;
    }
}

//----------------------------------------------------------------------------------------------------------------------

export default new RolesResourceAccess();

