import axios from 'axios';

//----------------------------------------------------------------------------------------------------------------------

export interface AdminStats
{
    users : number;
    characters : number;
}

//----------------------------------------------------------------------------------------------------------------------

class StatsResourceAccess
{
    async getAdminStats() : Promise<AdminStats>
    {
        const { data } = await axios.get('/api/admin/stats');
        return data;
    }
}

//----------------------------------------------------------------------------------------------------------------------

export default new StatsResourceAccess();

