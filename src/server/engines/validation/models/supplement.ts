// ---------------------------------------------------------------------------------------------------------------------
// Supplement Validation Model
// ---------------------------------------------------------------------------------------------------------------------

import { z } from 'zod';

// Models
import { HashID } from './common';

// ---------------------------------------------------------------------------------------------------------------------

export const SupplementID = HashID;

export const Supplement = z.object({
    id: SupplementID,
    name: z.string().min(1),
    owner: z.string().min(1)
        .optional(),
    scope: z.enum([ 'public', 'user' ]),
    official: z.boolean(),
}).passthrough();

// ---------------------------------------------------------------------------------------------------------------------
// Request Validations
// ---------------------------------------------------------------------------------------------------------------------

export const RouteParams = z.object({
    suppID: SupplementID,
});

