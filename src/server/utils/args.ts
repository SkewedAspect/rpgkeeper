// ---------------------------------------------------------------------------------------------------------------------
// Commandline Argument Parsing
// ---------------------------------------------------------------------------------------------------------------------

class ProgramArgsUtil
{
    #args : string[] = [];

    get args() : string[] { return this.#args; }

    constructor()
    {
        this.#args = process.argv.slice(2);
    }
}

// ---------------------------------------------------------------------------------------------------------------------

export default new ProgramArgsUtil();

