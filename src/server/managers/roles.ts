// Managers
import { table } from './database';

// Models
import { Role } from '../models/role';

// ---------------------------------------------------------------------------------------------------------------------

export async function list() : Promise<unknown>
{
    return (await table('role as r').select('r.role_id as id', 'r.name', 'r.permissions'))
        .map(Role.fromDB);
} // end list

