//----------------------------------------------------------------------------------------------------------------------
// Auth Router
//----------------------------------------------------------------------------------------------------------------------

import express from 'express';

//----------------------------------------------------------------------------------------------------------------------

const router = express.Router();

// Get Current User
router.get('/user', (req, resp) =>
{
    resp.json(req.user);
});

// Logout endpoint
router.post('/logout', (req, res, done) =>
{
    req.logout(done);
    res.end();
});


//----------------------------------------------------------------------------------------------------------------------

export default router;

